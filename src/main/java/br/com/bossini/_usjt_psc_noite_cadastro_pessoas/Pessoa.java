public class Pessoa {
    //cod_pessoa INT
    private int codigo;
    
    //nome VARCHAR(200)
    private String nome;
    
    //fone VARCHAR(50)
    private String fone;
    
    //email VARCHAR(200)
    private String email;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getFone() {
        return fone;
    }

    public void setFone(String fone) {
        this.fone = fone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
